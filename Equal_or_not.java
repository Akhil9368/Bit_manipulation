// Perform xor operation without using xor(operator)
public class Equal_or_Not {
    public static void main(String[] args) {
     int x =10;
     int y =10;
     if( (x^y) == 0){
         System.out.println("Both are Equals...");
     }
     else
         System.out.println("Both are different....");
    }
}
